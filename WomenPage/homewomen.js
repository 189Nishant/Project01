function handlepricesort(){
    var selected = document.querySelector("#selectTag").value;
    if(selected=="high"){
        data1.sort(function(a,b){
            return b.price-a.price;
        })
    }
    if(selected=="low"){
        data1.sort(function(a,b){
            return a.price-b.price;
        })
    }
    displaydata(data1);
};


function displaydata(data1){
    document.querySelector("#secounddiv").innerHTML="";
    data1.map(function(data1) {
        var div = document.createElement("div");
        var img = document.createElement("img");
        img.setAttribute("src",data1.img);
        var h3= document.createElement("h3");
        h3.innerText=data1.name;
        var p= document.createElement("p")
        p.innerText=data1.name2;
        var p2= document.createElement("p");
        p2.innerText='$ ' + data1.price;
        var p3= document.createElement("p");
        p3.innerText=data1.storecount;
        var btn1= document.createElement("button");
        btn1.innerText="Add To Card";
        btn1.setAttribute("id","btn1");
        btn1.addEventListener("click",function(){
            addToCart(data1);
        });
        div.append(img,h3,p,p2,p3,btn1);
        document.querySelector("#secounddiv").append(div);
    });
    
}

